DROP SCHEMA PUBLIC CASCADE;
DROP TABLE STUDENT IF EXISTS;
CREATE TABLE Student (
    Student_ID INT PRIMARY KEY,
    Name VARCHAR(255),
    Email VARCHAR(255),
    Password VARCHAR(255),
    Date_of_Birth DATE,
    PESEL VARCHAR(11)
);

DROP TABLE SOS IF EXISTS;
CREATE TABLE SOS (
    SOS_ID INT PRIMARY KEY,
    Name VARCHAR(255),
    Email VARCHAR(255),
    Password VARCHAR(255),
    Contact_Number VARCHAR(255)
);

DROP TABLE PRZEDMIOT IF EXISTS;
CREATE TABLE PRZEDMIOT (
    Przedmiot_ID INT PRIMARY KEY,
    Name VARCHAR(255)
);

